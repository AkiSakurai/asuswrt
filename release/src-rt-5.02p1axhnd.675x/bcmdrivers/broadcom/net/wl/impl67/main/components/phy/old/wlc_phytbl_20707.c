uint32
phy_get_chan_tune_tbl_20707(phy_info_t *pi, chanspec_band_t band,
	const chan_info_radio20707_rffe_t **chan_info_tbl)
{
	switch (RADIOREV(pi->pubpi->radiorev)) {
	case 0:
		if (band == WL_CHANSPEC_BAND_6G) {
			*chan_info_tbl = chan_tune_20707_rev0_6g;
			return ARRAYSIZE(chan_tune_20707_rev0_6g);
		} else if (band == WL_CHANSPEC_BAND_5G) {
			*chan_info_tbl = chan_tune_20707_rev0_5g;
			return ARRAYSIZE(chan_tune_20707_rev0_5g);
		} else {
			*chan_info_tbl = chan_tune_20707_rev0_2g;
			return ARRAYSIZE(chan_tune_20707_rev0_2g);
		}
		break;
	case 1:
		if (band == WL_CHANSPEC_BAND_6G) {
			*chan_info_tbl = chan_tune_20707_rev1_6g;
			return ARRAYSIZE(chan_tune_20707_rev1_6g);
		} else if (band == WL_CHANSPEC_BAND_5G) {
			if (PHY_IPA(pi)) {
				*chan_info_tbl = chan_tune_20707_rev1_ipa_5g;
				return ARRAYSIZE(chan_tune_20707_rev1_ipa_5g);
			} else {
				*chan_info_tbl = chan_tune_20707_rev1_epa_5g;
				return ARRAYSIZE(chan_tune_20707_rev1_epa_5g);
			}
		} else {
			*chan_info_tbl = chan_tune_20707_rev1_2g;
			return ARRAYSIZE(chan_tune_20707_rev1_2g);
		}
		break;
	default:
		PHY_ERROR(("wl%d: %s: Unsupported radio revision %d\n",
			pi->sh->unit, __FUNCTION__, RADIOREV(pi->pubpi->radiorev)));
		ASSERT(FALSE);
	}
	return 0;
}

