/* Get the right radio tuning table and return the number of elements
 * In this file so we can use ARRAYSIZE
 */
uint16 wlc_get_20709_chan_tune_table(phy_info_t *pi, chanspec_t chanspec,
		const chan_info_radio20709_rffe_t **p_chan_info_tbl)
{
	uint16 n_elems = 0;

	// Not used yet, to be used when the tables are split on band
	BCM_REFERENCE(chanspec);

	switch (RADIOREV(pi->pubpi->radiorev)) {
	case 0:
		*p_chan_info_tbl = chan_tune_20709_rev0;
		n_elems = ARRAYSIZE(chan_tune_20709_rev0);
		break;
	case 1:
		if (PHY_IPA(pi)) {
			*p_chan_info_tbl = chan_tune_20709_rev1_ipa;
			n_elems = ARRAYSIZE(chan_tune_20709_rev1_ipa);
		} else {
			*p_chan_info_tbl = chan_tune_20709_rev1_epa;
			n_elems = ARRAYSIZE(chan_tune_20709_rev1_epa);
		}
		break;
	default:
		PHY_ERROR(("wl%d: %s: Unsupported radio revision %d\n",
			pi->sh->unit, __FUNCTION__, RADIOREV(pi->pubpi->radiorev)));
		ASSERT(FALSE);
	}
	return n_elems;
}

